#int
inteiro = 8
print(type(inteiro))

#float
numero = 9.7
print(type(numero))

#bolean
ativo = True
print(type(ativo))

#string
nome = "Marcus Vinicius"
print(type(nome))

#list
lista = ["Marcus", "Goku", "Naruto", "Natsu"]
print(type(lista))


#dict
familia = {}
familia[0, 0] = "Marcus"
familia[0, 1] = "Goku"
familia[1, 0] = "Naruto"
familia[1, 1] = "Gustavo"

print(type(familia))
print(familia[1, 0])